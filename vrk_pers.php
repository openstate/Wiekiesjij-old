			<p>Stichting 'Het Nieuwe Stemmen' is opgericht door een groep twintigers met interesse voor politiek en een gedeelde verbazing over de (on-)mogelijkheden binnen die politiek. De gewoonten en procedures binnen onze democratie zijn eigenlijk betrekkelijk ondemocratisch te noemen, omdat ze niet op de juiste wijze worden ingezet. Partijen zijn de hoekstenen van de parlementair-democratische samenleving, maar zijn feitelijk bijzonder ondemocratisch. Bij maar heel weinig partijen hebben de 'gewone' leden zeggenschap over wie er op de kandidatenlijsten komen, in welke volgorde, etcetera.</p>
			<p>'Het Nieuwe Stemmen' wil daarin verandering brengen. Wij vrezen dat een brede politieke participatie er de komende jaren niet in zal zitten, al jaren nemen immers de ledenaantallen van de meeste politieke partijen af. Om de kiezers toch de mogelijkheid te geven betrokken te blijven en een stem te hebben komen wij met verschillende concepten die de politiek interessanter maakt en de kiezer de mogelijkheid geeft om meer weloverwogen met zijn of haar stem om te gaan.</p>
			<p>Allereerst waren daar de primaries of vrije lijsttrekkersverkiezingen, die niet alleen de leden van een bepaalde politieke partij de mogelijkheid geeft hun voorkeur uit te spreken over een lijsttrekkerskandidaat, maar daar ook de kiezers van die partij bij te betrekken. Het is immers vreemd dat bijvoorbeeld bij de VVD een paar duizend leden uitmaken wie de lijsttrekker wordt, terwijl uiteindelijk vele honderdduizenden mensen op die partij stemmen.</p>
			<p>Het volgende initiatief is de 'voorkeurstemhulp', die kiezers de mogelijkheid geeft zich eenvoudig en gericht te oriënteren op de vele kandidaten op een kandidatenlijst van een partij. Meer dan 80% van de kiezers brengt een stem uit op de lijsttrekker van de partij van zijn of haar keuze, terwijl misschien de nummer 33 van die lijst hem of haar veel meer zou aanspreken. De Tweede Kamer is een volksvertegenwoordiging, die idealiter bestaat uit een zo goed mogelijke representatie van het volk. Wij denken dat wanneer mensen de mogelijkheid krijgen om eenvoudig kandidaten te vergelijken op verschillende criteria zij meer weloverwogen hun stem zullen uitbrengen en niet automatisch op de lijsttrekker zullen stemmen. Iemand uit Drenthe zal misschien liever op een Drenth stemmen, maar heeft geen idee wie dat nu precies is. Of iemand woont in Amsterdam, is een alleenstaande vrouw met kinderen, heeft een eigen onderneming en wil graag stemmen op iemand die affiniteit heeft met de gezondheidszorg &#8211; na een simpele zoekactie op de genoemde (of andere) criteria komt de bezoeker van de site uit op de kandidaten die aan die criteria voldoen. Het maakt van de gekozen volksvertegenwoordiger iemand met een duidelijk mandaat, in plaats van iemand die op de slippen van de lijsttrekker de Kamer binnen komt.</p>
